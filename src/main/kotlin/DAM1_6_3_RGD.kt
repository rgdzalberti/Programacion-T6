/*



1 ¿Qué es un constructor y para qué lo usamos ?
Un constructor es el proceso que inicializa un objeto.

2 ¿Cuantos tipos de constructores nos podemos encontrar en kotlin y diferencias?
Dos tipos: El primario y el (o los) secundarios. El primario va junto al encabezado de la clase y
no puede contener código. El secundario hace uso del constructor interior de la clase y es usado
cuando el constructor primario no satisface las necesidades.

3 ¿De qué manera afecta y como hay que usar los constructores en la herencia?
Cuando se ejecuta un constructor heredado, también lo hará el constructor de la superclase.
Lo que se debe hacer es ejecutar en primer lugar el constructor de la clase que ocupa el nivel más
alto en la jerarquía de herencia y se continúa de forma ordenada con el resto de las subclases.


